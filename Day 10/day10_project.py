def add(n1, n2):
    return n1 + n2


def subtract(n1, n2):
    return n1 - n2


def multiply(n1, n2):
    return n1 * n2


def divide(n1, n2):
    return n1 / n2


operations = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide
}


num1 = float(input("Enter the first number: "))

for key in operations:
    print(key)
operation = input("pick an operation: ")
calculation_function = operations[operation]

num2 = float(input("Enter the second number: "))

result = calculation_function(num1, num2)

print(f"{num1} {operation} {num2} = {result}")

while True:
    is_continue = input(f"Type 'y' to continue calculating with {result} or type 'n' to exit. ").lower()
    if is_continue == 'y':
        operation = input("pick an operation: ")
        calculation_function = operations[operation]
        next_number = float(input("Enter the next number: "))
        final_result = calculation_function(result, next_number)
        print(f"{result} {operation} {next_number} = {final_result}")
        result = final_result
    else:
        exit()
