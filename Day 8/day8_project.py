re_run = True


def caesar(plain_text, shift_num, caesar_direction):
    msg = ""
    if caesar_direction == 'encode':
        for letter in plain_text:
            index_num = alphabet.index(letter)
            new_index = index_num + shift_num
            msg += alphabet[new_index]
        print(f"The encrypted message is : {msg}")
    elif caesar_direction == 'decode':
        for letter in plain_text:
            index_num = alphabet.index(letter)
            new_index = index_num - shift_num
            msg += alphabet[new_index]
        print(f"The decrypted message is : {msg}")
    else:
        print("Invalid Input!")


while re_run:
    direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
    text = input("Type your message:\n").lower()
    shift = int(input("Type the shift number:\n"))
    shift = shift % 27
    caesar(text, shift, direction)
    re_run_input = input("Do you want to continue? Yes/No. ").lower()
    if re_run_input == 'yes':
        re_run = True
    else:
        re_run = False
        print("Goodbye!!!")
